const User = require('../models/esquemas'); 





// Crear nuevos usuarios
const createUser = async (req, res) => {
  try {
    const user = new User(req.body);
    await user.save();
    res.status(201).send(user);
  } catch (error) {
    res.status(400).send(error);
  }
};

module.exports = {
  createUser
};